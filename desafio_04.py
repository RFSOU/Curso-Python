'''desafio 04 da aula 6, vamos aprender como funcionam os tipos primitivos
no Python e as peculiaridades do int() float() bool() e str(). Além disso,
veremos como fazer as primeiras operações com a função print() do Python.'''

n = (input("digite um valor "))
print('Este valor é Decimal?')
print (n.isdecimal())

print('Este valor é alfa numerico?')
print (n.isalnum())

print('Este valor é um espaço?')
print (n.isspace())

print('Este valor é um titulo?')
print (n.istitle())

